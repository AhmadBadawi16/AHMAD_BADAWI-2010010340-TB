    public void cbPilih() {
        try {
        java.sql.Connection conn = (Connection)koneksi.configDB();
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT pc, harga_per_jam FROM data_pc");

        boolean isExist = false;
        while (rs.next()) {
            for (int i = 0; i < cbPilih.getItemCount(); i++) {
                if (cbPilih.getItemAt(i).equals(rs.getString("pc"))) {
                    isExist = true;
                    break;
                }
            }
            if (!isExist) {
                cbPilih.addItem(rs.getString("pc"));
                txtHarga.setText(rs.getString("harga_per_jam"));
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }


}

    
