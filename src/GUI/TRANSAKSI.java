    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
         if (txtHarga.getText().equals("") || txtLamaSewa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Data belum lengkap", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
        try (Connection conn = (Connection) koneksi.configDB();
        PreparedStatement pstmt1 = conn.prepareStatement("INSERT INTO transaksi (id, nama, no_hp, alamat, pc, harga_per_jam, lama_sewa, tanggal_sewa, tanggal_selesai, total_harga) VALUES (?,?,?,?,?,?,?,?,?,?)");
        PreparedStatement pstmt2 = conn.prepareStatement("INSERT INTO customer (id, nama, no_hp, alamat) VALUES (?,?,?,?)")) {
        //transaksi
        pstmt1.setString(1, txtID.getText());
        pstmt1.setString(2, txtNama.getText());
        pstmt1.setString(3, txtHp.getText());
        pstmt1.setString(4, txtAlamat.getText());
        pstmt1.setString(5, (String) cbPilih.getSelectedItem());
        pstmt1.setString(6, txtHarga.getText());
        pstmt1.setString(7, txtLamaSewa.getText());
        pstmt1.setString(8, txtMulaiSewa.getText());
        pstmt1.setString(9, txtSelesaiSewa.getText());
        pstmt1.setString(10, txtTotal.getText());
        pstmt1.executeUpdate();
        //customer
        pstmt2.setString(1, txtID.getText());
        pstmt2.setString(2, txtNama.getText());
        pstmt2.setString(3, txtHp.getText());
        pstmt2.setString(4, txtAlamat.getText());
        pstmt2.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        table();
        reset();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }     
    }
   
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        if (txtHarga.getText().equals("") || txtLamaSewa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Data belum lengkap", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                int harga = Integer.parseInt(txtHarga.getText());
                int lama_sewa = Integer.parseInt(txtLamaSewa.getText());
                int total_harga = harga * lama_sewa;
                txtTotal.setText(Integer.toString(total_harga));
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Input harus berupa angka","Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnHitungActionPerformed

